function changeLanguage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        // Fallback to English if key missing in target lang
        const text = (resources[lang] && resources[lang][key]) || (resources['en'] && resources['en'][key]) || key;

        if (el.tagName === 'INPUT' && el.getAttribute('placeholder')) {
            el.placeholder = text;
        } else {
            el.innerText = text;
        }
    });
    // Store preference
    localStorage.setItem('netra_lang', lang);
}

// Init
window.addEventListener('load', () => {
    const lang = localStorage.getItem('netra_lang') || 'en';
    const selector = document.getElementById('langSelector');
    if (selector) selector.value = lang;
    changeLanguage(lang);
});
